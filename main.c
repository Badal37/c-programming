
#include <stdio.h>

int main()
{
    int a,b;
   
    printf("Enter your number");
    
    scanf("%d%d",&a,&b);
    
    printf("Addition is %d\n",a+b);
    
    printf("Subtraction is %d\n",a-b);
    
    printf("Multiplication is %d\n",a*b);
    
    printf("Quotient is %d\n",a/b);
    
    printf("Remainder is %d",a%b);
    
    return 0;
}
